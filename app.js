let web3, contract, userAccount;

// Kết nối MetaMask
document.getElementById("connectWallet").addEventListener("click", async () => {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        const accounts = await ethereum.request({ method: "eth_requestAccounts" });
        userAccount = accounts[0];

        document.getElementById("walletStatus").innerText = `Connected: ${userAccount.slice(0, 6)}...${userAccount.slice(-4)}`;
        contract = new web3.eth.Contract(contractABI, contractAddress);

        updateBalance();
        updateCollateral();
    } else {
        alert("Please install MetaMask!");
    }
});

// Cập nhật số dư IRONv2
async function updateBalance() {
    const balance = await contract.methods.balanceOf(userAccount).call();
    document.getElementById("balance").innerText = web3.utils.fromWei(balance, "ether");
}

// Cập nhật ETH thế chấp
async function updateCollateral() {
    const collateral = await contract.methods.getCollateral(userAccount).call();
    document.getElementById("collateral").innerText = web3.utils.fromWei(collateral, "ether") + " ETH";
}

// Gửi ETH thế chấp
document.getElementById("depositBtn").addEventListener("click", async () => {
    const amount = document.getElementById("depositAmount").value;
    await contract.methods.depositCollateral().send({ from: userAccount, value: web3.utils.toWei(amount, "ether") });
    updateCollateral();
});

// Đúc IRONv2
document.getElementById("mintBtn").addEventListener("click", async () => {
    const amount = document.getElementById("mintAmount").value;
    await contract.methods.mint(web3.utils.toWei(amount, "ether")).send({ from: userAccount });
    updateBalance();
});

// Đốt IRONv2
document.getElementById("burnBtn").addEventListener("click", async () => {
    const amount = document.getElementById("burnAmount").value;
    await contract.methods.burn(web3.utils.toWei(amount, "ether")).send({ from: userAccount });
    updateBalance();
    updateCollateral();
});
