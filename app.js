window.addEventListener('load', () => {
    if (typeof window.ethereum !== 'undefined') {
        web3 = new Web3(window.ethereum);
        document.getElementById('connectButton').addEventListener('click', connectMetaMask);
    } else {
        alert('Please install MetaMask!');
    }
});

async function connectMetaMask() {
    try {
        // Yêu cầu kết nối ví MetaMask
        const accounts = await ethereum.request({ method: 'eth_requestAccounts' });
        userAccount = accounts[0];
        document.getElementById('walletAddress').innerText = `Connected: ${userAccount}`;
        contract = new web3.eth.Contract(contractABI, contractAddress);
    } catch (error) {
        console.error('Error connecting to MetaMask:', error);
    }
}

document.getElementById('depositButton').addEventListener('click', async () => {
    const amount = prompt('Enter the amount of ETH to deposit:');
    if (amount && userAccount) {
        try {
            const tx = await contract.methods.depositCollateral().send({
                from: userAccount,
                value: web3.utils.toWei(amount, 'ether')
            });
            document.getElementById('status').innerText = `Deposited ${amount} ETH`;
        } catch (error) {
            document.getElementById('status').innerText = `Error: ${error.message}`;
        }
    } else {
        alert('Please connect your MetaMask wallet first!');
    }
});

document.getElementById('mintButton').addEventListener('click', async () => {
    const amount = prompt('Enter the amount of IRON to mint:');
    if (amount && userAccount) {
        try {
            const tx = await contract.methods.mint(web3.utils.toWei(amount, 'ether')).send({
                from: userAccount
            });
            document.getElementById('status').innerText = `Minted ${amount} IRON`;
        } catch (error) {
            document.getElementById('status').innerText = `Error: ${error.message}`;
        }
    } else {
        alert('Please connect your MetaMask wallet first!');
    }
});

